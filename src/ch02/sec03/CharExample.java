package ch02.sec03;

public class CharExample {
    public static void main(String[] args) {
        char c1 = 'A';
        char c2 = 65;
        char c3 = '가';
        char c4 = 44032;

        System.out.println(c1); // A
        System.out.println(c2); // A
        System.out.println(c3); // 가
        System.out.println(c4); // 가
    }
}
