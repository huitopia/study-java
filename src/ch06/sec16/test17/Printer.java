package ch06.sec16.test17;

public class Printer {
    static int val1;
    static boolean val2;
    static double val3;
    static String val4;

    static void println(int val1) {
        System.out.println("val1 = " + val1);
    }

    static void println(boolean val2) {
        System.out.println("val2 = " + val2);
    }

    static void println(double val3) {
        System.out.println("val3 = " + val3);
    }

    static void println(String val4) {
        System.out.println("val4 = " + val4);
    }
}
